-- Script SQL d'ajout des régions et départements Français

CREATE TABLE IF NOT EXISTS region (
  region_id INT PRIMARY KEY NOT NULL,
  region_nom VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS departement (
  departement_code VARCHAR(3) PRIMARY KEY NOT NULL,
  departement_region INT NOT NULL,
  departement_nom VARCHAR(255) NOT NULL,
  CONSTRAINT fk_departement_region
  FOREIGN KEY (departement_region)
  REFERENCES region(region_id)
  ON UPDATE CASCADE
);

